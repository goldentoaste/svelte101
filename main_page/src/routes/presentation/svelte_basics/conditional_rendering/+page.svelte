<script>
    import Frame from "$lib/components/Frame.svelte";
    import Prism from "svelte-prism";


    let showItem = $state(false);
    let foodType = $state("pizza");
</script>

<h1 class="title">Conditional Rendering (If block)</h1>

<p>
    Svelte uses the <a target="_blank" href="https://svelte.dev/docs/svelte/if">{"{#if ...}"}</a> syntax to do conditional rendering. It
    is similar to if else statements in most languages and can be nested. This avoids
    need for nested ternery like in JSX (ü§Æ {"{showA ? <CompA/> : (showB ? <CompB/> : '??')}... or how about {showA && <CompA/>} "}).
</p>

<Prism language="svelte">
    {`<script>
    let showItem = $state(false);
    let foodType = $state("pizza");
<\/script>

<button onclick={() => (showItem = !showItem)}>Toggle label</button>
<button
    onclick={() => (foodType = foodType === "pizza" ? "burger" : "pizza")}
>
    Toggle food
</button>

{#if showItem}
    <h2>Hello!</h2>
{/if}

{#if foodType === "pizza"}
    <p>I want pizza! üçï</p>
{:else if 5 < 2}
    <p>???</p>
{:else}
    <p>I actually want burger! üçî</p>
{/if}
`}
</Prism>

